#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	loop;
	int	i;
	int	temp;

	loop = 0;
	i = 0;
	while (loop < size)
	{
		i = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
			}
			i++;
		}
		loop++;
	}
}
